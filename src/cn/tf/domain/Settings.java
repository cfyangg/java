package cn.tf.domain;

public class Settings {
	private String Id;
	private String Name;
	private String Value;
	
	public  Settings() {	

	}

	public String getId() {
		return Id;
	}

	public void setId(String id) {
		Id = id;
	}

	public String getName() {
		return Name;
	}

	public void setName(String name) {
		Name = name;
	}

	public String getValue() {
		return Value;
	}

	public void setValue(String value) {
		Value = value;
	}
	
}
