package cn.tf.domain;

public class Publishers {
	private String Id;
	private String Name;
	public Publishers() {
		
	}
	public String getId() {
		return Id;
	}
	public void setId(String id) {
		Id = id;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	
}
