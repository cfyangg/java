package cn.tf.domain;

public class VidoFile {
	 private String Id;
	 private String Title;
	 private String FivPath;
	 private String FileExt;
	 
	 public VidoFile(){
		 
	 }
	 
	public String getId() {
		return Id;
	}
	public void setId(String id) {
		Id = id;
	}
	public String getTitle() {
		return Title;
	}
	public void setTitle(String title) {
		Title = title;
	}
	public String getFivPath() {
		return FivPath;
	}
	public void setFivPath(String fivPath) {
		FivPath = fivPath;
	}
	public String getFileExt() {
		return FileExt;
	}
	public void setFileExt(String fileEx) {
		FileExt = fileEx;
	}
	 
		
	
	
	

	
}


