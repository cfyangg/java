package cn.tf.domain;

public class UserStates {
	private String Id;
	private String Name;
	public  UserStates() {}
	public String getId() {
		return Id;
	}
	public void setId(String id) {
		Id = id;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	
	

}
